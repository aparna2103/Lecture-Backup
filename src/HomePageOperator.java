    private void Inboxbtn1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_Inboxbtn1MouseClicked
        String oldPass = new String(oldPassword.getPassword());
        String newPass = new String(newPassword.getPassword());
        String CNewPass = new String(CNewPassword.getPassword());
        String id = SignIn.id;

        SessionFactory sf=new Configuration().configure().buildSessionFactory();
        Session session = sf.openSession();
        Transaction  tx = session.beginTransaction();
        Criteria crit=session.createCriteria(OperatorTable.class);
        crit.add(Restrictions.and(Restrictions.eq("opID",id),Restrictions.eq("opPassword",oldPass)));
        List<OperatorTable> result=crit.list();
        if(result.isEmpty())
        {
            InvalidPWD.setForeground(Color.red);
            InvalidPWD.setText("*Invalid Password");
        }
        else
        {
            if(newPass.equals(CNewPass))
            {
                OperatorTable st=(OperatorTable)session.get(OperatorTable.class,id);
                st.setOpPassword(newPass);
                session.update(st);
                tx.commit();
                JOptionPane.showMessageDialog(this,"Password changed successfully...");
                new SignIn().setVisible(true);
                dispose();
            }
            else
            {
                JOptionPane.showMessageDialog(this,"New Password and Confirm New Password Does Not Match");
                InvalidPWD1.setForeground(Color.red);
                InvalidPWD1.setText("*Password Coes not match new password");
            }
        }
    }//GEN-LAST:event_Inboxbtn1MouseClicked

