    private void showNewsData()
    {
        SessionFactory sf=new Configuration().configure().buildSessionFactory();
        Session session=sf.openSession();
        Transaction tx=session.beginTransaction();
        Criteria crit = session.createCriteria(NewsTable.class);
        //crit.add(Restrictions.eq("stuName",SignIn.id));
        List<NewsTable> data=crit.list();
        DefaultTableModel dtm= (DefaultTableModel)jTable3.getModel();
        dtm.setRowCount(0);
        for(NewsTable st:data)
        {
            Object obj[]={st.getSerialNo(),st.getName(),st.getCaption(),st.getDescription(),st.getPostDate()};
            dtm.addRow(obj);
        }
    }
    
